def multiples_of_3_and_5(n):
    sum = 0
    for i in range(1, n):
        if (i % 3) == 0 or (i % 5) == 0:
            sum += i
    return sum

# (3 points)
def test2():
    tc = unittest.TestCase()
    tc.assertEqual(multiples_of_3_and_5(10), 23)
    tc.assertEqual(multiples_of_3_and_5(500), 57918)
    tc.assertEqual(multiples_of_3_and_5(1000), 233168)

